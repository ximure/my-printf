#include "libftprintf.h"

void	ft_format_digital(int n)
{
	if (n == -2147483648)
		write(1, "-2147483648", 11);
	if (n < 0 && n != -2147483648)
	{
		write(1, "-", 1);
		n = n * -1;
	}
	if (n >= 10)
	{
		ft_format_digital(n / 10);
		ft_format_digital(n % 10);
	}
	if (n < 10 && n != -2147483648)
	{
		n = n + '0';
		write(1, &n, 1);
	}
}
